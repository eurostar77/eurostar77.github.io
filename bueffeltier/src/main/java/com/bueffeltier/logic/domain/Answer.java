package com.bueffeltier.logic.domain;

public class Answer
{
	private Long id;

	private String text;

	public Answer()
	{

	}

	public Long getId()
	{
		return id;
	}

	public void setId(Long id)
	{
		this.id = id;
	}

	public String getText()
	{
		return text;
	}

	public void setText(String text)
	{
		this.text = text;
	}
}
